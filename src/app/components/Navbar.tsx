"use client";

import Link from "next/link";
import { usePathname } from 'next/navigation';
import AuthButton from './AuthButton';
import { useSession } from 'next-auth/react';
import { motion } from 'framer-motion';

const ADMIN_IDS = ['1297171796180865049', 'YOUR_DISCORD_ID_2']; // Add your Discord IDs here

export default function Navbar() {
  const pathname = usePathname();
  const { data: session } = useSession();

  const navLinks = [
    { name: 'Home', href: '/' },
    { name: 'About', href: '/about' },
    { name: 'Documentation', href: '/documentation' },
    { name: 'Applications', href: '/applications' },
  ];

  // Add Admin Dashboard link if user is an admin
  if (session && ADMIN_IDS.includes(session.user?.id!)) {
    navLinks.push({ name: 'Admin', href: '/admin' });
  }

  return (
    <motion.nav
      initial={{ opacity: 0, y: -50 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.5 }}
      className="sticky top-0 z-50 bg-black/80 backdrop-blur-sm border-b border-white/10"
    >
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="flex items-center justify-between h-16">
          <div className="flex items-center">
            <Link href="/" className="flex items-center text-white text-6xl font-bold hover:text-[#00c6ff] transition-all duration-300">
              <LogoIcon className="mr-2 w-12 h-12" />
            </Link>
          </div>
          <div className="hidden md:flex items-center space-x-8">
            {navLinks.map((link, index) => (
              <motion.div
                key={link.name}
                initial={{ opacity: 0, x: -20 }}
                animate={{ opacity: 1, x: 0 }}
                transition={{ delay: index * 0.1, duration: 0.3 }}
              >
                <Link
                  href={link.href}
                  className={`relative px-3 py-2 text-sm font-medium group transition-all duration-300 ${
                    pathname === link.href ? 'text-white' : 'text-gray-300 hover:text-white'
                  }`}
                >
                  {link.name}
                  <span className={`absolute -bottom-1 left-0 h-0.5 bg-[#00c6ff] transition-all duration-300 ${
                    pathname === link.href ? 'w-full' : 'w-0 group-hover:w-full'
                  }`}></span>
                </Link>
              </motion.div>
            ))}
            <AuthButton />
          </div>
        </div>
      </div>
    </motion.nav>
  );
}

